    private void showListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_showListValueChanged
        // TODO add your handling code here:
        
        if(evt.getValueIsAdjusting()) return;
        Show show = showList.getSelectedValue();
        if(show == null) return;
        
        titleTxt.setText(show.getTitle());
        qualityTxt.setText(show.getQuality().name());
        genreTxt.setText(show.getGenre().name());
        languageTxt.setText(show.getLang().name());
        numberOfViewsTxt.setText(String.valueOf(show.getNum_views()));
        synopsysTxt.setText(show.getSynopsys());
        averageRatingTxt.setText(String.valueOf(show.getAverage_rating()));
        totalWatchTxt.setText(show.getTotal_watch());
        
        
        
    }//GEN-LAST:event_showListValueChanged

