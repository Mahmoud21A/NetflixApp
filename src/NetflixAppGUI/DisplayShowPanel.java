    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        
        String title = titleTxt.getText();
        if(title.isEmpty()){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid title.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String quality = qualityTxt.getText();
        if(!(quality.equalsIgnoreCase(Quality.HD.name()) || quality.equalsIgnoreCase(Quality.ULTRA_HD.name()))){
           JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid quality.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
         }

        
        String genre = genreTxt.getText();
        if(!(genre.equalsIgnoreCase(Genre.COMEDY.name()) || genre.equalsIgnoreCase(Genre.DRAMA.name()))){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid genre.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String language = languageTxt.getText();
        if(!(language.equalsIgnoreCase(Language.ENGLISH.name()) || language.equalsIgnoreCase(Language.FRENCH.name()) || language.equalsIgnoreCase(Language.SPANISH.name()))){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid language.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }

        
        String numberOfViewsStr = numberOfViewsTxt.getText();
        int numberOfViews;
        try{
            numberOfViews = Integer.parseInt(numberOfViewsStr);
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, "" + "Please provide a valid Number of Views", ""
                   + "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String synopsys = synopsysTxt.getText();
        if(synopsys.isEmpty()){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid synopsys.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String averageRatingStr = averageRatingTxt.getText();
        double averageRating;
        try{
            averageRating = Double.parseDouble(averageRatingStr);
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, "" + "Please provide a valid Average Rating", ""
                   + "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String totalWatch = totalWatchTxt.getText();
        if(totalWatch.isEmpty()){
            JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Please provide a valid total watch time.", "Editing Show", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Show show = new Show(title, Quality.valueOf(quality), Genre.valueOf(genre), Language.valueOf(language), numberOfViews, synopsys, averageRating, totalWatch);
        int selectedIndex = showList.getSelectedIndex();
        model.set(selectedIndex, show);
        
        JOptionPane.showMessageDialog(DisplayShowPanel.this, ""
            + "Show edited successfully", "Editing Show", JOptionPane.INFORMATION_MESSAGE);
        
    }//GEN-LAST:event_editBtnActionPerformed


